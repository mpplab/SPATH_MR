package OLA;

public class EiColumn {

	int EiStart;
	int EiEnd;
	
	public EiColumn(){};
	
	// 设置EiStart
	public void setEiStart(int _EiStart){
		EiStart = _EiStart;
	}
	
	// 设置EiEnd
	public void setEiEnd(int _EiEnd){
		EiEnd = _EiEnd;
	}
	
	// 获取EiStart
	public int getEiStart(){
		return EiStart;
	}
	
	// 获取EiEnd
	public int getEiEnd(){
		return EiEnd;
	}
	
}
